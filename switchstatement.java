public class switchstatement {
    public static void main(String[] args) {
        int number = 2;
        switch (number) {
            case 1:
                System.out.println("agrim");
                break;
            case 2:
                System.out.println("asfdsfgrim");
                break;
            case 3:
                System.out.println("agrwefsdim");
                break;
            case 4:
                System.out.println("agasdarim");
                break;
            default:
                System.out.println("agriafsam");
        }
    }
}